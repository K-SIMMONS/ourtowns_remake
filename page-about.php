      <p class="bruhp">Our Towns Civic Foundation is an IRS-certified 501(c)3 nonprofit organization, founded on the idea and belief that the sources of American renewal are mainly at the community level. The purpose of the foundation is to make the success of a renewal era more probable. Toward this end we will report on and share the stories of local innovators; distill and share the lessons of their successes (or failures); promote connections among like-minded people around the country, in virtual and real-world venues; encourage new journalistic models (and technologies) to cover local stories and their implication; and in other ways to help now-disconnected innovators and reformers realize that they’re part of something larger.</p>

